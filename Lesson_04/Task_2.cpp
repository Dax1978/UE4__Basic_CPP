//                                                                                                                //
// Весьиа интересная задача с точки зрения алгоритмов. Достаточно полпулярная, и имеет много алгоритмов,          //
// где пытаются найти самый оптимальный и бысмтрый алгорит.                                                       //
//                                                                                                                //
